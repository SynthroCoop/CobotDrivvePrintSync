package coop.synthro.print;

/**
 *
 * @author thorsten
 */
public class PrintUser {

    private String userID;
    private String userLOGIN;
    private String userFULLNAME;
    private String userEMAIL;
    private String userCODE;
    private String userPIN;
    private String userLOCKED;
    private String userPUK;

    /**
     * @return the userID
     */
    public String getUserID() {
        return userID;
    }

    /**
     * @param userID the userID to set
     */
    public void setUserID(String userID) {
        this.userID = userID;
    }

    /**
     * @return the userLOGIN
     */
    public String getUserLOGIN() {
        return userLOGIN;
    }

    /**
     * @param userLOGIN the userLOGIN to set
     */
    public void setUserLOGIN(String userLOGIN) {
        this.userLOGIN = userLOGIN;
    }

    /**
     * @return the userFULLNAME
     */
    public String getUserFULLNAME() {
        return userFULLNAME;
    }

    /**
     * @param userFULLNAME the userFULLNAME to set
     */
    public void setUserFULLNAME(String userFULLNAME) {
        this.userFULLNAME = userFULLNAME;
    }

    /**
     * @return the userEMAIL
     */
    public String getUserEMAIL() {
        return userEMAIL;
    }

    /**
     * @param userEMAIL the userEMAIL to set
     */
    public void setUserEMAIL(String userEMAIL) {
        this.userEMAIL = userEMAIL;
    }

    /**
     * @return the userCODE
     */
    public String getUserCODE() {
        return userCODE;
    }

    /**
     * @param userCODE the userCODE to set
     */
    public void setUserCODE(String userCODE) {
        this.userCODE = userCODE;
    }

    /**
     * @return the userPIN
     */
    public String getUserPIN() {
        return userPIN;
    }

    /**
     * @param userPIN the userPIN to set
     */
    public void setUserPIN(String userPIN) {
        this.userPIN = userPIN;
    }

    /**
     * @return the userLOCKED
     */
    public String getUserLOCKED() {
        return userLOCKED;
    }

    /**
     * @param userLOCKED the userLOCKED to set
     */
    public void setUserLOCKED(String userLOCKED) {
        this.userLOCKED = userLOCKED;
    }

    /**
     * @return the userPUK
     */
    public String getUserPUK() {
        return userPUK;
    }

    /**
     * @param userPUK the userPUK to set
     */
    public void setUserPUK(String userPUK) {
        this.userPUK = userPUK;
    }
}
